function getVerse(mood) {
    let verses;
    switch(mood) {
        case 'happy':
            verses = happyVerses;
            break;
        case 'angry':
            verses = angryVerses;
            break;
        case 'sad':
            verses = sadVerses;
            break;
        default:
            verses = [];
    }
    const randomIndex = Math.floor(Math.random() * verses.length);
    const verseDisplay = document.getElementById('verse');
    verseDisplay.innerText = verses[randomIndex];
}
